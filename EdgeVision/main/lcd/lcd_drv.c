/* ─── Internal: draw one 8×8 glyph at 2× scale (16×16 px per char cell) ── */
static void lcd_drv_draw_char(uint16_t x, uint16_t y, char ch, uint16_t fg, uint16_t bg)
{
    if (x + 16 > LCD_H_RES || y + 16 > LCD_V_RES) return;
    unsigned idx = (unsigned char)ch;
    if (idx < 0x20 || idx > 0x7F) idx = '?';
    const uint8_t *glyph = s_font8x8[idx - 0x20];

    /* Build a 16×16 pixel buffer; each source pixel → 2×2 block.
     * Pixels must be big-endian (high byte first) for ST7789 SPI. */
    static uint16_t cbuf[16 * 16];
    const uint16_t fg_sw = __builtin_bswap16(fg);
    const uint16_t bg_sw = __builtin_bswap16(bg);

    for (int gy = 0; gy < 8; gy++) {
        uint8_t row = glyph[gy];
        for (int gx = 0; gx < 8; gx++) {
            uint16_t pix = (row & (0x80u >> gx)) ? fg_sw : bg_sw;
            cbuf[(gy * 2)     * 16 + gx * 2]     = pix;
            cbuf[(gy * 2)     * 16 + gx * 2 + 1] = pix;
            cbuf[(gy * 2 + 1) * 16 + gx * 2]     = pix;
            cbuf[(gy * 2 + 1) * 16 + gx * 2 + 1] = pix;
        }
    }
    esp_lcd_panel_draw_bitmap(s_panel, x, y, x + 16, y + 16, cbuf);
}

/* ─────────────────────────────────────────────────────────────────────────
 * lcd_drv_init
 *
 * ST7789 init sequence via esp_lcd (SPI2_HOST, GPIO-matrix routed pins):
 *
 *  1. SPI bus — MOSI + SCLK on non-native pins (GPIO matrix, ≤40MHz)
 *  2. Panel IO — DC + CS managed by esp_lcd SPI driver
 *  3. Panel driver — ST7789 reset + init registers
 *  4. Gap correction — ST7789 chip is 240 wide, panel is 170 wide
 *                      GRAM offset = (240−170)/2 = 35 columns
 * ───────────────────────────────────────────────────────────────────────── */
esp_err_t lcd_drv_init(void)
{
    /* 0. Backlight: drive BLK HIGH before anything else so we can confirm
     *    the panel is powered even before SPI init completes. */
    gpio_config_t blk_cfg = {
        .pin_bit_mask = 1ULL << LCD_PIN_BLK,
        .mode         = GPIO_MODE_OUTPUT,
        .pull_up_en   = GPIO_PULLUP_DISABLE,
        .pull_down_en = GPIO_PULLDOWN_DISABLE,
        .intr_type    = GPIO_INTR_DISABLE,
    };
    gpio_config(&blk_cfg);
    gpio_set_level(LCD_PIN_BLK, 1);  /* backlight ON */
    ESP_LOGI(TAG, "BLK GPIO%d → HIGH (backlight on)", LCD_PIN_BLK);

    /* 1. Init SPI bus (no MISO — display is write-only) */
    spi_bus_config_t bus_cfg = {
        .mosi_io_num     = LCD_PIN_SDA,
        .miso_io_num     = -1,
        .sclk_io_num     = LCD_PIN_SCL,
        .quadwp_io_num   = -1,
        .quadhd_io_num   = -1,
        .max_transfer_sz = LCD_H_RES * LCD_V_RES * sizeof(uint16_t),
    };
    ESP_RETURN_ON_ERROR(
        spi_bus_initialize(SPI2_HOST, &bus_cfg, SPI_DMA_CH_AUTO),
        TAG, "SPI bus init failed");

    /* 2. Create SPI panel IO (handles DC + CS signalling automatically) */
    esp_lcd_panel_io_handle_t io_handle = NULL;
    esp_lcd_panel_io_spi_config_t io_cfg = {
        .dc_gpio_num        = LCD_PIN_DC,
        .cs_gpio_num        = LCD_PIN_CS,
        .pclk_hz            = LCD_SPI_CLK_HZ,
        .lcd_cmd_bits       = 8,
        .lcd_param_bits     = 8,
        .spi_mode           = 0,
        .trans_queue_depth  = 10,
    };
    ESP_RETURN_ON_ERROR(
        esp_lcd_new_panel_io_spi((esp_lcd_spi_bus_handle_t)SPI2_HOST, &io_cfg, &io_handle),
        TAG, "Panel IO create failed");

    /* 3. Create ST7789 panel driver */
    esp_lcd_panel_dev_config_t panel_cfg = {
        .reset_gpio_num  = LCD_PIN_RES,
        .rgb_ele_order   = LCD_RGB_ELEMENT_ORDER_RGB,
        .bits_per_pixel  = 16,
    };
    ESP_RETURN_ON_ERROR(
        esp_lcd_new_panel_st7789(io_handle, &panel_cfg, &s_panel),
        TAG, "Panel create failed");

    /* 4. Init sequence */
    ESP_RETURN_ON_ERROR(esp_lcd_panel_reset(s_panel),       TAG, "Panel reset failed");
    ESP_RETURN_ON_ERROR(esp_lcd_panel_init(s_panel),        TAG, "Panel init failed");
    ESP_RETURN_ON_ERROR(esp_lcd_panel_invert_color(s_panel, true), TAG, "Invert color failed");

    /* Gap: ST7789 chip GRAM is 240 wide, panel is 170 wide.
     * Physical offset = (240-170)/2 = 35 columns → gap=(35,0). */
    ESP_RETURN_ON_ERROR(esp_lcd_panel_set_gap(s_panel, 35, 0), TAG, "Set gap failed");

    ESP_RETURN_ON_ERROR(esp_lcd_panel_disp_on_off(s_panel, true), TAG, "Display on failed");

    ESP_LOGI(TAG, "ST7789 %dx%d OK — SPI @%dMHz, SCL=%d MOSI=%d DC=%d CS=%d RST=%d",
             LCD_H_RES, LCD_V_RES, LCD_SPI_CLK_HZ / 1000000,
             LCD_PIN_SCL, LCD_PIN_SDA, LCD_PIN_DC, LCD_PIN_CS, LCD_PIN_RES);
    return ESP_OK;
}

/* ─────────────────────────────────────────────────────────────────────────
 * lcd_drv_draw_rgb565
 *
 * Send a contiguous RGB565 bitmap to the display region (x,y)→(x+w, y+h).
 * Data must be big-endian (bytes swapped) — set swap_color_bytes=1 when
 * decoding JPEG with espressif__esp_jpeg.
 * ───────────────────────────────────────────────────────────────────────── */
esp_err_t lcd_drv_draw_rgb565(uint16_t x, uint16_t y,
                               uint16_t w, uint16_t h,
                               const uint16_t *buf)
{
    if (!s_panel) return ESP_ERR_INVALID_STATE;
    return esp_lcd_panel_draw_bitmap(s_panel, x, y, x + w, y + h, buf);
}

/* ─────────────────────────────────────────────────────────────────────────
 * lcd_drv_fill
 *
 * Fill the entire screen with a solid color.
 * Uses a single-row stack buffer to avoid heap allocation.
 * ───────────────────────────────────────────────────────────────────────── */
void lcd_drv_fill(uint16_t color)
{
    if (!s_panel) return;

    /* ESP32 is little-endian; SPI sends bytes in memory order (LSB first).
     * ST7789 expects RGB565 big-endian (MSB first).  Swap once here so
     * callers can pass natural color values (0xF800=red, 0x07E0=green…). */
    uint16_t sw = __builtin_bswap16(color);
    uint16_t line[LCD_H_RES];
    for (int i = 0; i < LCD_H_RES; i++) line[i] = sw;

    for (int y = 0; y < LCD_V_RES; y++) {
        esp_lcd_panel_draw_bitmap(s_panel, 0, y, LCD_H_RES, y + 1, line);
    }
}

/* ─────────────────────────────────────────────────────────────────────────
 * lcd_drv_puts
 *
 * Draw a null-terminated ASCII string at (x, y).
 * Each glyph is 16×16 pixels (8×8 font at 2× scale).
 * Stops drawing at screen edge.
 * ───────────────────────────────────────────────────────────────────────── */
void lcd_drv_puts(uint16_t x, uint16_t y, const char *str, uint16_t fg, uint16_t bg)
{
    if (!s_panel || !str) return;
    uint16_t cx = x;
    while (*str && cx + 16 <= LCD_H_RES) {
        lcd_drv_draw_char(cx, y, *str, fg, bg);
        cx += 16;
        str++;
    }
}

/* ─────────────────────────────────────────────────────────────────────────
 * lcd_drv_printf
 *
 * printf-formatted text at (x, y), same 2× glyph scale as lcd_drv_puts.
 * Buffer limited to 64 characters.
 * ───────────────────────────────────────────────────────────────────────── */
void lcd_drv_printf(uint16_t x, uint16_t y, uint16_t fg, uint16_t bg,
                    const char *fmt, ...)
{
    char buf[64];
    va_list ap;
    va_start(ap, fmt);
    vsnprintf(buf, sizeof(buf), fmt, ap);
    va_end(ap);
    lcd_drv_puts(x, y, buf, fg, bg);
}
